package kr.co.future.dom.model;

import kr.co.future.api.FieldOption;
import kr.co.future.confdb.CollectionName;

@CollectionName("global-config")
public class GlobalConfig {
	@FieldOption(nullable = false, length = 60)
	private String key;

	@FieldOption(nullable = false)
	private Object value;

	private boolean isHidden; // UI should not know this

	public String getKey() {
		return key;
	}

	public void setKey(String key) {
		this.key = key;
	}

	public Object getValue() {
		return value;
	}

	public void setValue(Object value) {
		this.value = value;
	}

	public boolean isHidden() {
		return isHidden;
	}

	public void setHidden(boolean isHidden) {
		this.isHidden = isHidden;
	}
}
